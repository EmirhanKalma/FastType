let dictionary = ["apple", "cloud", "dance", "earth", "field", "glass", "heart", "light", "music", "ocean", "river", "sleep", "table", "voice", "water"];


let totalPlayers = 0;
let currentPlayerIndex = 0;
let results = [];

let charIndex = 0;
let mistakes = 0;
let isStarted = false;
let timeLeft = 15;
let timerInterval = null;
let startTime = null;
let scrollY = 0;

const wordsWrapper = document.querySelector(".words-wrapper");
const timerDisplay = document.getElementById("timer");
const cursor = document.querySelector(".cursor");

// --- ЛОГИКА ТЕМ ---
function applyTheme(themeName) {
    const theme = themes[themeName];
    const root = document.documentElement;
    root.style.setProperty("--bg-color", theme.bg);
    root.style.setProperty("--text-color", theme.text);
    root.style.setProperty("--panel-color", theme.panel);
    root.style.setProperty("--sub-color", theme.sub);
    root.style.setProperty("--main-color", theme.main);
    root.style.setProperty("--caret-color", theme.main);
    localStorage.setItem("selectedTheme", themeName);
}

// Вызывай эту функцию, если захочешь кнопку переключения
function nextTheme() {
    const themeKeys = Object.keys(themes);
    const current = localStorage.getItem("selectedTheme") || "NightNeon";
    const nextIndex = (themeKeys.indexOf(current) + 1) % themeKeys.length;
    applyTheme(themeKeys[nextIndex]);
}

// --- ИГРОВАЯ ЛОГИКА ---
function setupMultiplayer() {
    totalPlayers = parseInt(document.getElementById('player-total').value);
    if (totalPlayers < 1) return;
    document.getElementById('setup-area').classList.add('hidden');
    showNameInput();
}

function showNameInput() {
    document.getElementById('player-label').innerText = `Игрок ${currentPlayerIndex + 1}`;
    document.getElementById('nickname-input').value = "";
    document.getElementById('name-area').classList.remove('hidden');
}

function prepareRound() {
    document.getElementById('name-area').classList.add('hidden');
    document.getElementById('game-area').classList.remove('hidden');
    initGame();
}

function initGame() {
    clearInterval(timerInterval);
    charIndex = 0;
    mistakes = 0;
    isStarted = false;
    timeLeft = 15;
    scrollY = 0;
    wordsWrapper.style.transform = `translateY(0px)`;
    
    generateWords();
    timerDisplay.textContent = timeLeft;
    updateCursor();
}

function generateWords() {
  wordsWrapper.innerHTML = "";

  for (let i = 0; i < 100; i++) {
    let wordText = dictionary[Math.floor(Math.random() * dictionary.length)];
    let wordDiv = document.createElement("div");
    wordDiv.className = "word"; 
    wordDiv.style.display = "inline-block";

    [...wordText].forEach(char => {
      let span = document.createElement("span");
      span.textContent = char;
      wordDiv.appendChild(span);
    });

    let spaceSpan = document.createElement("span");
    spaceSpan.textContent = " ";
    wordDiv.appendChild(spaceSpan);

    wordsWrapper.appendChild(wordDiv);
  }
}



document.addEventListener("keydown", (e) => {
    const gameArea = document.getElementById('game-area');
    if (gameArea.classList.contains('hidden')) return;

    const spans = wordsWrapper.querySelectorAll("span");

    if (!isStarted && e.key.length === 1) {
        isStarted = true;
        startTime = Date.now();
        timerInterval = setInterval(() => {
            timeLeft--;
            timerDisplay.textContent = timeLeft;
            if (timeLeft <= 0) finishRound();
        }, 1000);
    }

    if (isStarted) {
        if (e.key === "Backspace") {
            if (charIndex > 0) {
                charIndex--;
                spans[charIndex].className = "";
            }
        } else if (e.key.length === 1) {
            const current = spans[charIndex];
            if (e.key === current.textContent) {
                current.className = "correct";
            } else {
                current.className = "incorrect";
                mistakes++;
            }
            charIndex++;

            if (current.offsetTop > 48 + Math.abs(scrollY)) {
                scrollY -= 48;
                wordsWrapper.style.transform = `translateY(${scrollY}px)`;
            }
        }
        updateCursor();
    }
});

function finishRound() {
    clearInterval(timerInterval);   
    const dur = (Date.now() - startTime) / 60000;
    let wpm = Math.round((charIndex / 5) / dur) || 0;
    const acc = charIndex > 0 ? Math.round(((charIndex - mistakes) / charIndex) * 100) : 0;
    if (acc < 10) {
        wpm = 0;
    }
    const nick = document.getElementById('nickname-input').value || `Игрок ${currentPlayerIndex + 1}`;
    results.push({ nick, wpm, acc });
    document.getElementById('game-area').classList.add('hidden');
    if (++currentPlayerIndex < totalPlayers) {
        showNameInput();
    } else {
        showFinalScoreboard();
    }
}

function showFinalScoreboard() {
    const resultArea = document.getElementById('result-area');
    const resultsList = document.getElementById('results-list');
    
    resultArea.style.display = "block";
    resultsList.innerHTML = results.map(res => `
        <div style="border-bottom: 2px solid var(--sub-color); padding: 15px 0;">
            <div class="res-label">${res.nick}</div>
            <div class="res-num">${res.wpm} <span style="font-size: 1.5rem">WPM</span></div>
            <div style="color: var(--text-color); opacity: 0.6;">Точность: ${res.acc}%</div>
        </div>
    `).join("");
}

// При загрузке применяем сохраненную тему
window.onload = () => {
    const savedTheme = localStorage.getItem("selectedTheme") || "NightNeon";
    applyTheme(savedTheme);
};